    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String origem = (String)jComboBox1.getSelectedItem();
    String formato = "";
        if(jCheckBoxjson.isSelected())
            formato+="JSON; ";
        if(jCheckBoxcsv.isSelected())
            formato+="CSV; ";
        if(jCheckBoxplanilhas.isSelected())
            formato+="Planilhas; ";
        if(jCheckBoxtabelas.isSelected())
            formato+="Tabelas; ";
        if(jCheckBoxpdf.isSelected())
            formato+="PDF; ";
        if(jCheckBoxaudio.isSelected())
            formato+="Audio; ";
        if(jCheckBoxtexto.isSelected())
            formato+="Texto; ";
        
    String sistema = "";
        if(jCheckBoxERP.isSelected())
            sistema+="ERP; ";
        if(jCheckBoxvendas.isSelected())
            sistema+="Vendas; ";
        if(jCheckBoxoutros.isSelected())
            sistema+=jTextField1.getText();
        
    String volume_json = "";
        if(jCheckBoxjson.isSelected())
            volume_json+=jTxtvolumejson.getText();
    String freq_json = "";
        if(jCheckBoxjson.isSelected())
            freq_json+= jTxtfreqjson.getText();
        
    String volume_csv = "";
        if(jCheckBoxcsv.isSelected())
            volume_csv+=jTxtvolumecsv.getText();
    String freq_csv = "";
        if(jCheckBoxcsv.isSelected())
            freq_csv+= jTxtfreqcsv.getText();
        
    String volume_planilhas = "";
        if(jCheckBoxplanilhas.isSelected())
            volume_planilhas+=jTxtvolumeplanilhas.getText();
    String freq_planilhas = "";
        if(jCheckBoxplanilhas.isSelected())
            freq_planilhas+= jTxtfreqplanilhas.getText();
        
    String volume_tabelas = "";
        if(jCheckBoxtabelas.isSelected())
            volume_tabelas+=jTxtvolumetabelas.getText();
    String freq_tabelas = "";
        if(jCheckBoxtabelas.isSelected())
            freq_tabelas+= jTxtfreqtabelas.getText();
        
    String volume_pdf = "";
        if(jCheckBoxpdf.isSelected())
            volume_pdf+=jTxtvolumepdf.getText();
    String freq_pdf = "";
        if(jCheckBoxpdf.isSelected())
            freq_pdf+= jTxtfreqpdf.getText();
        
    String volume_audio = "";
        if(jCheckBoxaudio.isSelected())
            volume_audio+=jTxtvolumeaudio.getText();
    String freq_audio = "";
        if(jCheckBoxaudio.isSelected())
            freq_audio+= jTxtfreqaudio.getText();
        
    String volume_texto = "";
        if(jCheckBoxtexto.isSelected())
            volume_texto+=jTxtvolumetexto.getText();
    String freq_texto = "";
        if(jCheckBoxtexto.isSelected())
            freq_texto+= jTxtfreqtexto.getText();
        
    
    Bronze usuarioluiz = new Bronze(origem, formato, sistema, volume_json, freq_json, volume_csv, 
            freq_csv, volume_planilhas, freq_planilhas, volume_tabelas, freq_tabelas, volume_pdf, 
            freq_pdf, volume_audio, freq_audio, volume_texto, freq_texto);
        
        try {
            Connection conexao = new Conexao().getConnection();
            BronzeDAO cadastro = new BronzeDAO(conexao);
            cadastro.insert(usuarioluiz);
        
        } catch (SQLException ex) {
            Logger.getLogger(FormEscopo.class.getName()).log(Level.SEVERE, null, ex);
        }
     
    }//GEN-LAST:event_jButton1ActionPerformed

