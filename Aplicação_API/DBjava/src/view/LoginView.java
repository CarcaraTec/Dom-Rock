    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        
        String usuario = jTextUsuario1.getText();
        String senha = jTextUsuario.getText();

        Usuario usuarioluiz = new Usuario(usuario, senha);

        try {
            Connection conexao = new Conexao().getConnection();
            UsuarioDAO cadastro =  new UsuarioDAO(conexao);
            cadastro.insert(usuarioluiz);
            JOptionPane.showMessageDialog(null,"Usuario cadastrado com sucesso.","Aviso",JOptionPane.INFORMATION_MESSAGE);
            jPcadastro.setVisible(false);
        } catch (SQLException ex) {
            Logger.getLogger(Inicio.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

