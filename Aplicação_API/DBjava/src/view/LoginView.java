    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        try { 
            controller.autenticar(); 
            if(jTextFieldUsuario.isVisible()){
           
            }
            else{
                crudsql crud = new crudsql();
                UsuarioUP us = new UsuarioUP();
                crud.mostrarUsuario(jTextFieldUsuario.getText());
                jNivelAcesso0.setText(us.getNivelAcesso());
                
                String admin = "Administrador";
                String sup = "Suporte";
                String user = "Usuario";

                if(jNivelAcesso0.getText().equals(admin)){
                    InicioAdmin adm = new InicioAdmin();
                    adm.setVisible(true);
                    dispose();
                }
                if(jNivelAcesso0.getText().equals(sup)){
                    InicioSuporte suport  = new InicioSuporte();
                    suport.setVisible(true);
                    dispose();
        }
                if(jNivelAcesso0.getText().equals(user)){
                    Inicio usuario = new Inicio();
                    usuario.setVisible(true);
                    dispose();
        }
               
            }
        } catch (SQLException ex) {
            Logger.getLogger(Inicio.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

